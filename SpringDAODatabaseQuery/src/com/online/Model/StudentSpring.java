package com.online.Model;

public class StudentSpring {
	private String name;
	private String pwd;
	private int age;
	private String gender;
	private String hobbiesCompact;
	private String technologyCompact;
	private String address;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getHobbiesCompact() {
		return hobbiesCompact;
	}
	public void setHobbiesCompact(String hobbiesCompact) {
		this.hobbiesCompact = hobbiesCompact;
	}
	public String getTechnologyCompact() {
		return technologyCompact;
	}
	public void setTechnologyCompact(String technologyCompact) {
		this.technologyCompact = technologyCompact;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}

	
}
